#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	len_src;

	i = 0;
	len_src = ft_strlen(src);
	if (len_src >= size && size > 0)
	{
		while (i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	else if (len_src < size)
	{
		while (i < len_src + 1)
		{
			dest[i] = src[i];
			i++;
		}
	}
	return (len_src);
}
