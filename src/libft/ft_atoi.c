#include "libft.h"

int	ft_atoi(const char *p)
{
	int	ans;
	int	sign;

	ans = 0;
	sign = 1;
	while (*p == 32 || (*p >= 9 && *p <= 13))
		p++;
	if (*p == '+')
		p++;
	else if (*p == '-')
	{
		sign = -1;
		p++;
	}
	while (*p >= '0' && *p <= '9')
	{
		ans = ans * 10 + (*p - 48);
		p++;
	}
	return (ans * sign);
}
